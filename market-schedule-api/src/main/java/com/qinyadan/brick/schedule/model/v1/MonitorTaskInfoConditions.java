package com.qinyadan.brick.schedule.model.v1;

public class MonitorTaskInfoConditions {
	
	public String monitorId;
	public String monitorName;
	public int monitorType;
	public int monitorStatus;
	/**
	 * @return the monitorId
	 */
	public String getMonitorId() {
		return monitorId;
	}
	/**
	 * @param monitorId the monitorId to set
	 */
	public void setMonitorId(String monitorId) {
		this.monitorId = monitorId;
	}
	/**
	 * @return the monitorName
	 */
	public String getMonitorName() {
		return monitorName;
	}
	/**
	 * @param monitorName the monitorName to set
	 */
	public void setMonitorName(String monitorName) {
		this.monitorName = monitorName;
	}
	/**
	 * @return the monitorType
	 */
	public int getMonitorType() {
		return monitorType;
	}
	/**
	 * @param monitorType the monitorType to set
	 */
	public void setMonitorType(int monitorType) {
		this.monitorType = monitorType;
	}
	/**
	 * @return the monitorStatus
	 */
	public int getMonitorStatus() {
		return monitorStatus;
	}
	/**
	 * @param monitorStatus the monitorStatus to set
	 */
	public void setMonitorStatus(int monitorStatus) {
		this.monitorStatus = monitorStatus;
	}
	
	
}
